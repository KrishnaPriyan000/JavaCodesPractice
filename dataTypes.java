class dataTypes{

	public static void main(String[] args){
 
		byte fNum = 5;
		System.out.println(fNum);

		short sNum = 15;
		System.out.println(sNum);
		
		int tNum = 25;
		System.out.println(tNum);

		long foNum = 35L;
		System.out.println(foNum);

		float fiNum = 3.14f;
		System.out.println(fiNum);

		double siNum = 3.14176726;
		System.out.println(siNum);

		char falp = 'A';
		System.out.println(falp);

		boolean a = true;

	}
}
